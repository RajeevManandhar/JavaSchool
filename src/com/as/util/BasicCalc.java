package com.as.util;

public class BasicCalc {

	public int add(int i, int j) {
		int sum = i + j;
		return sum;
	}

	public int add(int i, int j, int k) {
		return i + j + k;
	}

	public int subtract(int i, int j) {
		return i - j;
	}

	public int multiply(int i, int j) {
		return i * j;

	}

	public int div(int i, int j) {
		return i / j;
	}
}
